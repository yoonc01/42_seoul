#include <unistd.h>

void	ft_putstr_non_printable(char *str)
{
	while (*str != '\0')
	{
		if (' ' <= *str && *str <= '~')
			write(1, str, 1);
		else
		{
			write(1, "\\", 1);
			write(1, &"0123456789abcdef"[(*str) / 16], 1);
			write(1, &"0123456789abcdef"[(*str) % 16], 1);
		}
		str++;
	}
}
