#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	idx;

	idx = 0;
	while (*s != '\0')
	{
		(*f)(idx, s);
		idx++;
		s++;
	}
}
