int	ft_sqrt(int nb)
{
	long long	n;

	n = 0;
	while (n*n < nb)
		n++;
	if (n * n == nb)
		return (n);
	return (0);
}

#include <stdio.h>

int	main()
{
	printf("%d", ft_sqrt(2147483647));
}
