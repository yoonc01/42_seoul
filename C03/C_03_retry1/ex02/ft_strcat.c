char	*ft_strcat(char *dest, char *src)
{
	char	*ans;

	ans = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = *src;
	return (ans);
}
