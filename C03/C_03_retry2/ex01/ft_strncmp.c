int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (0 < n)
	{
		if (*s1 != *s2 || *s1 == '\0' || *s2 == '\0')
		{
			if (*s1 > *s2)
				return (1);
			else if (*s1 < *s2)
				return (-1);
			else
				return (0);
		}
		n--;
	}
	return (0);
}
